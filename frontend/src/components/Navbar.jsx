import React, { useState } from 'react';
import { Link, useNavigate, useLocation } from 'react-router-dom';
import useAuthStore from '../store/authStore';
import NotificationBell from './NotificationBell';

const Navbar = () => {
  const { user, logout } = useAuthStore();
  const navigate = useNavigate();
  const location = useLocation();
  const [showMobileMenu, setShowMobileMenu] = useState(false);

  const isActive = (path) => {
    if (path === '/') return location.pathname === '/';
    return location.pathname.startsWith(path);
  };

  const handleLogout = () => {
    logout();
    navigate('/login');
  };

  return (
    <nav className="bg-white shadow-lg border-b sticky top-0 z-50" style={{position: 'sticky', top: '0px'}}>
      <div className="max-w-7xl mx-auto px-2 sm:px-4">
        <div className="flex justify-between items-center min-h-16 h-16">
          <div className="flex items-center space-x-2 sm:space-x-8">
            <button
              onClick={() => setShowMobileMenu(!showMobileMenu)}
              className="lg:hidden p-2 rounded-md text-gray-700 hover:bg-gray-100"
            >
              <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                {showMobileMenu ? (
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
                ) : (
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
                )}
              </svg>
            </button>
            
            <Link to="/" className="flex items-center flex-shrink-0">
              <img src="/deep.png" alt="Deep Infotech" className="h-8 sm:h-10 lg:h-12 w-auto" />
            </Link>
            
            <div className="hidden lg:flex space-x-1 xl:space-x-4">
              <Link
                to="/"
                className={`relative px-2 xl:px-3 py-2 text-xs xl:text-sm font-medium transition-all duration-300 ${
                  isActive('/') 
                    ? 'text-blue-600 font-semibold' 
                    : 'text-gray-700 hover:text-blue-600'
                }`}
              >
                Dashboard
                {isActive('/') && (
                  <div className="absolute bottom-0 left-0 right-0 h-0.5 bg-gradient-to-r from-blue-400 via-blue-600 to-purple-600 rounded-full"></div>
                )}
              </Link>
              
              <Link
                to="/carry-in-service"
                className={`relative px-2 xl:px-3 py-2 text-xs xl:text-sm font-medium whitespace-nowrap transition-all duration-300 ${
                  isActive('/carry-in-service') 
                    ? 'text-blue-600 font-semibold' 
                    : 'text-gray-700 hover:text-blue-600'
                }`}
              >
                CarryInService
                {isActive('/carry-in-service') && (
                  <div className="absolute bottom-0 left-0 right-0 h-0.5 bg-gradient-to-r from-blue-400 via-blue-600 to-purple-600 rounded-full"></div>
                )}
              </Link>
              
              {user?.role === 'HOST' && (
                <>
                  <Link
                    to="/users"
                    className={`relative px-2 xl:px-3 py-2 text-xs xl:text-sm font-medium whitespace-nowrap transition-all duration-300 ${
                      isActive('/users') 
                        ? 'text-blue-600 font-semibold' 
                        : 'text-gray-700 hover:text-blue-600'
                    }`}
                  >
                    Role Management
                    {isActive('/users') && (
                      <div className="absolute bottom-0 left-0 right-0 h-0.5 bg-gradient-to-r from-blue-400 via-blue-600 to-purple-600 rounded-full"></div>
                    )}
                  </Link>
                  <Link
                    to="/customers"
                    className={`relative px-2 xl:px-3 py-2 text-xs xl:text-sm font-medium whitespace-nowrap transition-all duration-300 ${
                      isActive('/customers') 
                        ? 'text-blue-600 font-semibold' 
                        : 'text-gray-700 hover:text-blue-600'
                    }`}
                  >
                    Customers
                    {isActive('/customers') && (
                      <div className="absolute bottom-0 left-0 right-0 h-0.5 bg-gradient-to-r from-blue-400 via-blue-600 to-purple-600 rounded-full"></div>
                    )}
                  </Link>
                  <Link
                    to="/analytics"
                    className={`relative px-2 xl:px-3 py-2 text-xs xl:text-sm font-medium whitespace-nowrap transition-all duration-300 ${
                      isActive('/analytics') 
                        ? 'text-blue-600 font-semibold' 
                        : 'text-gray-700 hover:text-blue-600'
                    }`}
                  >
                    Engineer Analytics
                    {isActive('/analytics') && (
                      <div className="absolute bottom-0 left-0 right-0 h-0.5 bg-gradient-to-r from-blue-400 via-blue-600 to-purple-600 rounded-full"></div>
                    )}
                  </Link>
                  <Link
                    to="/settings/categories"
                    className={`relative px-2 xl:px-3 py-2 text-xs xl:text-sm font-medium whitespace-nowrap transition-all duration-300 ${
                      isActive('/settings') 
                        ? 'text-blue-600 font-semibold' 
                        : 'text-gray-700 hover:text-blue-600'
                    }`}
                  >
                    Categories
                    {isActive('/settings') && (
                      <div className="absolute bottom-0 left-0 right-0 h-0.5 bg-gradient-to-r from-blue-400 via-blue-600 to-purple-600 rounded-full"></div>
                    )}
                  </Link>
                </>
              )}
            </div>
          </div>

          <div className="flex items-center space-x-0.5 sm:space-x-2">
            <NotificationBell />
            
            <span className="text-xs sm:text-sm text-gray-700">
              {user?.username} ({user?.role})
            </span>
            
            <Link
              to="/profile"
              className={`relative hidden sm:block px-1 sm:px-2 py-2 text-xs sm:text-sm font-medium transition-all duration-300 ${
                isActive('/profile') 
                  ? 'text-blue-600 font-semibold' 
                  : 'text-gray-700 hover:text-blue-600'
              }`}
            >
              Profile
              {isActive('/profile') && (
                <div className="absolute bottom-0 left-0 right-0 h-0.5 bg-gradient-to-r from-blue-400 via-blue-600 to-purple-600 rounded-full"></div>
              )}
            </Link>
            
            <button
              onClick={handleLogout}
              className="bg-red-600 text-white px-1.5 sm:px-3 py-2 rounded-md text-xs sm:text-sm font-medium hover:bg-red-700"
            >
              Logout
            </button>
          </div>
        </div>

        {/* Mobile Menu */}
        {showMobileMenu && (
          <div className="lg:hidden border-t border-gray-200 py-2">
            <div className="flex flex-col space-y-1">
              <Link
                to="/"
                onClick={() => setShowMobileMenu(false)}
                className="text-gray-700 hover:bg-gray-100 px-3 py-2 rounded-md text-sm font-medium"
              >
                Dashboard
              </Link>
              
              <Link
                to="/profile"
                onClick={() => setShowMobileMenu(false)}
                className="text-gray-700 hover:bg-gray-100 px-3 py-2 rounded-md text-sm font-medium"
              >
                Profile
              </Link>
              
              <Link
                to="/carry-in-service"
                onClick={() => setShowMobileMenu(false)}
                className="text-gray-700 hover:bg-gray-100 px-3 py-2 rounded-md text-sm font-medium"
              >
                CarryInService
              </Link>
              
              {user?.role === 'HOST' && (
                <>
                  <Link
                    to="/users"
                    onClick={() => setShowMobileMenu(false)}
                    className="text-gray-700 hover:bg-gray-100 px-3 py-2 rounded-md text-sm font-medium"
                  >
                    Role Management
                  </Link>
                  <Link
                    to="/customers"
                    onClick={() => setShowMobileMenu(false)}
                    className="text-gray-700 hover:bg-gray-100 px-3 py-2 rounded-md text-sm font-medium"
                  >
                    Customers
                  </Link>
                  <Link
                    to="/analytics"
                    onClick={() => setShowMobileMenu(false)}
                    className="text-gray-700 hover:bg-gray-100 px-3 py-2 rounded-md text-sm font-medium"
                  >
                    Engineer Analytics
                  </Link>
                  <Link
                    to="/settings/categories"
                    onClick={() => setShowMobileMenu(false)}
                    className="text-gray-700 hover:bg-gray-100 px-3 py-2 rounded-md text-sm font-medium"
                  >
                    Categories
                  </Link>
                </>
              )}
            </div>
          </div>
        )}
      </div>
    </nav>
  );
};

export default Navbar;